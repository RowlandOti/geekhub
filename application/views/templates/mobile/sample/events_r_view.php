            <h3>EXHIBITION: Hi 5</h3>
            <img src="http://events.stanford.edu/events/412/41273/41273.jpg" width="150" alt="" />
            <p>The Department of Art &amp; Art History at Stanford University presents <em>Hi 5,</em><strong> </strong>on view from January 14 to February 23, 2014, with a reception on Thursday, January 23, 2014, from 5:30-7:30 PM, at the Thomas Welton Stanford
                Art Gallery. This group exhibition introduces the latest cohort of first-year MFA students in Art Practice: Eko F&#233;lix, Einat Imber, Christopher Nickel, Felicita Norris, and Lauren Ashley Toomer.</p>
            <p>Curator Gail Wight notes that &#8220;each of these unique and talented artists brings a fresh interpretation to historical and contemporary cultural milieu through varied mediums, strategies, and points of view.&#8221;</p>
            <p>Eko F&#233;lix, born in Mexico to loving parents of Cuban and Japanese descent, left Mexico City in 2013 to continue her study of art practice in the Unites States. F&#233;lix&#8217;s primary interest lies with fiction and its connection to
                reality. Exhibiting a group of colorful, realistic 8&#8221; x 10&#8221; paintings, Eko F&#233;lix&#8217;s small portraits are influenced by Mexico&#8217;s best-selling magazine - a publication that features fictional TV stars&#8217; lifestyles,
                diets, interviews, and gossip. Juxtaposed against those magazine pages are images of TV spectators, often of humble origins with ailing bodies, who are used as unglamorous examples of heroes that strive forward in the face of adversity.</p>
            <p>Einat Imber makes objects that call attention to movement - that tell a story of actions that happened earlier or are about to unfold, and often times highlights contradictions. With the mindset that every action has a reaction, Imber has
                created a few pieces that toy with that relationship. One of her latest works is a larger-than-life balloon that is setup in the gallery rotunda. A slide projector is mounted across the room casting light on the balloon, which creates
                an eclipsing shadow and helps to further her artistic exploration of cause and effect.</p>
            <p>Christopher Nickel&#8217;s large and slightly ominous black and white panels tower along the gallery walls. The panels are comprised of 36&#8221; x 36&#8221; printed squares, pieced together to depict 12 foot disheveled concrete columns. Once
                a structural support and an iconic image of a modernist utopia society, these decaying columns are now a monument to past civilization. With this set of work, Nickel reminds the viewer of present-day society&#8217;s constant drive for
                technological advancement, and the residue it leaves behind.</p>
            <p>Dramatic and bold, displaying vibrant, toxic color, Felicita Norris&#8217;s paintings explore emotional states and relationships &#8211; specifically the dark overlapping of disturbing realities with fantasies - while focusing on a loss of
                innocence and desire. Her large-scale works, some standing at five, six, or even eight feet tall, depict intimate familial situations that could be described as violent in nature. Not necessarily literal, and more a delving into and personification
                of the psyche, Norris&#8217;s work stems from her actual circumstances and life experiences as she re-stages her life through these orchestrated and, at times, disturbing scenes.</p>
            <p>Lauren Ashley Toomer&#8217;s recent drawings, which are figurative in nature and primarily graphite on paper, are inspired by her investigation of what is contextually viable and ethical through art. Pushing the delicate line between art and
                exploitation, she uses medical texts, morgue reports, and images on publicly accessible databases to compassionately present these portraits &#8211; often of children &#8211; as not merely a face, but a life, through the addition of the
                deceased&#8217;s mementos and belongings. Toomer will also showcase her sculpture work by exhibiting a large collection of masks, in varying sizes, that she has created using bamboo and reed.</p>
